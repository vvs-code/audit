    define('COEFFTONUM', $coeff_to_num);

    /**
     * @param $audit
     * @param $checklists
     * @param $coeff_to_num
     * @return array
     */
    function get_final_for_checklist ($audit, $checklists = CHECKLISTS, $coeff_to_num = COEFFTONUM) {
        $sumfinal = 0;
        $finals = [];

        $full = true;

        for ($i = 0; $i <= 9; $i++) {
            if ($audit['checklists'][$i]) {

                $marks = $audit['marks'];

                $unedited = (count(array_filter($marks[$i], function ($value) {
                    return $value === -1;
                })));

                if ($unedited === 0) {
                    $resmark = 0;

//                    var_dump($i);
                    foreach ($checklists[$i]['categories'][0] as $j => $category) {

                        foreach ($checklists[$i]['categories'][1][$j] as $criteria) {
                            $thismark = $marks[$i][$criteria];
                            $w = $checklists[$i]['weights'][$criteria];
                            $resmark += $thismark === -2 ? 0 : ($thismark === -1 ? 0 : $thismark * $w);
                        }

                    }

                    $maxmarksum = 0;

                    foreach ($checklists[$i]['weights'] as $j => $weight) {
                        if ($marks[$i][$j] !== -2) {
                            $maxmarksum += $weight;
                        }
                    }

                    if ($maxmarksum === 0) {
                        $maxmarksum = 1;
                    }

                    $checklistweight = $audit['weights'][$i];
                    $coeff = $coeff_to_num[$audit['coeff']];
                    $wmark = round($checklistweight * ($resmark / $maxmarksum * 100), 3);
                    $final = round($wmark * $coeff, 2);
                    $sumfinal += $final;
                    $finals[] = $final;
                } else {
                    $finals[] = 0;
                    $full = false;
                }
            } else {
                $finals[] = 0;
            }
        }

        if ($sumfinal >= 86) {
            $class = 'A';
        } elseif ($sumfinal >= 70) {
            $class = 'B';
        } elseif ($sumfinal >= 50) {
            $class = 'C';
        } else {
            $class = 'D';
        }

        return [
